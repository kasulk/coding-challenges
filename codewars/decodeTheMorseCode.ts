export function decodeMorse(morseCode: string): string {
  // remove whitespace from start and end
  // turn input into array of morse words and loop through it
  // for each morse word
  // turn each morse word into array of morse characters, and loop through it
  // for each morse character
  // return the value of the property that matches the morse character
  // turn the letter arrays back into strings of words
  // turn the words array back into string, return it

  return morseCode
    .trim()
    .split("   ")
    .map((morseWord) =>
      morseWord
        .split(" ")
        .map((morseChar) => MORSE_CODE[morseChar])
        .join("")
    )
    .join(" ");
}

